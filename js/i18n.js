const I18N = {
  lang: 'es',
  t(key) {
    return (TRANSLATIONS[this.lang] || TRANSLATIONS['es'])[key] || key;
  },
  detect() {
    const saved = localStorage.getItem('neuralgpt_lang');
    if (saved && TRANSLATIONS[saved]) { this.lang = saved; return; }
    const browser = (navigator.language || navigator.userLanguage || 'es').slice(0,2).toLowerCase();
    this.lang = TRANSLATIONS[browser] ? browser : 'es';
  },
  ensureSelector() {
    if (document.getElementById('lang-select')) return;
    const nav = document.querySelector('.main-nav, .nav');
    if (!nav) return;
    const wrap = document.createElement('div');
    wrap.className = 'lang-selector';
    const sel = document.createElement('select');
    sel.id = 'lang-select';
    sel.setAttribute('aria-label', this.t('label_language'));
    LANG_OPTIONS.forEach(([code, label]) => {
      const opt = document.createElement('option');
      opt.value = code;
      opt.textContent = label;
      sel.appendChild(opt);
    });
    sel.addEventListener('change', () => this.set(sel.value));
    wrap.appendChild(sel);
    nav.appendChild(wrap);
  },
  applyHrefTexts() {
    document.querySelectorAll('a[href]').forEach(el => {
      const href = el.getAttribute('href');
      const key = LINK_KEY_BY_HREF[href];
      if (key && TRANSLATIONS.es[key] !== undefined) {
        el.textContent = this.t(key);
      }
    });
  },
  applyLiteralTexts() {
    const map = LITERAL_TEXT[this.lang];
    if (!map || !document.body) return;
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    const nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach(node => {
      const parent = node.parentElement;
      if (!parent) return;
      const tag = parent.tagName;
      if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT') return;
      const raw = node.textContent || '';
      const trimmed = raw.trim();
      const translated = map[trimmed];
      if (!translated) return;
      const leading = raw.match(/^\s*/)[0];
      const trailing = raw.match(/\s*$/)[0];
      node.textContent = leading + translated + trailing;
    });
    const titleTranslated = map[document.title];
    if (titleTranslated) document.title = titleTranslated;
  },
  applyAttributeLiterals() {
    const map = ATTRIBUTE_LITERALS[this.lang];
    if (!map || !document.body) return;
    const placeholders = map.placeholder || {};
    const aria = map['aria-label'] || {};
    document.querySelectorAll('[placeholder]').forEach(el => {
      const raw = el.getAttribute('placeholder');
      const translated = placeholders[raw];
      if (translated) el.setAttribute('placeholder', translated);
    });
    document.querySelectorAll('[aria-label]').forEach(el => {
      const raw = el.getAttribute('aria-label');
      const translated = aria[raw];
      if (translated) el.setAttribute('aria-label', translated);
    });
  },
  bindMutationTranslation() {
    if (this._observerBound || !document.body) return;
    this._observerBound = true;
    const observer = new MutationObserver(() => {
      if (this.lang !== 'en') return;
      this.applyLiteralTexts();
      this.applyAttributeLiterals();
      this.applyHrefTexts();
    });
    observer.observe(document.body, { childList: true, subtree: true });
  },
  set(lang) {
    if (!TRANSLATIONS[lang]) return;
    this.lang = lang;
    localStorage.setItem('neuralgpt_lang', lang);
    this.apply();
  },
  apply() {
    document.documentElement.lang = this.lang;
    this.ensureSelector();
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });
    document.querySelectorAll('[data-i18n-href]').forEach(el => {
      el.href = el.dataset.i18nHref.replace('{lang}', this.lang);
    });
    this.applyHrefTexts();
    this.applyLiteralTexts();
    this.applyAttributeLiterals();
    this.bindMutationTranslation();
    if (this.lang === 'ar') {
      document.body.setAttribute('dir', 'rtl');
    } else {
      document.body.removeAttribute('dir');
    }
    const sel = document.getElementById('lang-select');
    if (sel) sel.value = this.lang;
  },
  init() {
    this.detect();
    this.apply();
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
